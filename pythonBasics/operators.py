#Mathematical Operators

print("Mathematical Operators")

#Addition
addition = 5 + float(input("5 + "))
print(addition)

#Substraction
subtraction = 10 - float(input("10 - "))
print(subtraction)

#multiplication
multiplication = 2 * float(input("2 * "))
print(multiplication)

#division
division = 20 / float(input("20 / "))
print(division)

#integerDivision
integerDivision = 20 // float(input("20 // ")) #returns the result of the division discarding the decimals
print(integerDivision)

#modulo
modulo = 10 % float(input("10 % "))
print(modulo)

#exponentiation
exponentiation = 2 ** float(input("2 ** "))
print(exponentiation)


#Logical Operators

print("Logical Operators")

#And operator
num = float(input("(5 > 3) and (10 < "))
print(5 > 3) and (10 < num)

#Or operator
num = float(input("(8 < 10) or (5 > "))
print((8 < 10) or (5 > num))

#Not operator
num = float(input("not (2 > "))
print(not (2 > num))


#Comparison Operators

print("Comparison Operators")

#Greater than
num = float(input("1 > "))
print((1 > num))

#Smaller than
num = float(input("3 < "))
print(3 < num)

#Equal to
num = float(input("4 == "))
print(4 == num)